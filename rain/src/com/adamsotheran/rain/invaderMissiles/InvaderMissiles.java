package com.adamsotheran.rain.invaderMissiles;

public class InvaderMissiles {

	public int x;
	public int y;
	public int vel;
	public int id;
	public boolean isVisible;
	public int damage;


	public InvaderMissiles(int x, int y, int vel, int id, boolean isVisible, int damage) {
		this.x = x;
		this.y = y;
		this.vel = vel;
		this.id = id;
		this.isVisible = isVisible;
		this.damage = damage;
	}

}

